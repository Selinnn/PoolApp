package com.poolapp.poolapp;

public class ayVeucret {
    String ay;
    String ucret;

    public ayVeucret(String ay, String ucret) {
        this.ay = ay;
        this.ucret = ucret;
    }

    public String getAy() {
        return ay;
    }

    public void setAy(String ay) {
        this.ay = ay;
    }

    public String getUcret() {
        return ucret;
    }

    public void setUcret(String ucret) {
        this.ucret = ucret;
    }
}
